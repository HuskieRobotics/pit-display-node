// Define a variable of type string
let message: string = "Hello, TypeScript!";

// Define a variable of type number
let count: number = 10;

// Define a variable of type boolean
let isDone: boolean = false;

// Define an array of numbers
let numbers: number[] = [1, 2, 3, 4, 5];

// Define an object with properties
let person: { name: string; age: number } = {
  name: "John Doe",
  age: 30,
};

// Define a function that takes two numbers and returns their sum
function addNumbers(a: number, b: number): number {
  return a + b;
}

// Call the function and store the result in a variable
let result: number = addNumbers(5, 10);

// Print the variables to the console
console.log(message);
console.log(count);
console.log(isDone);
console.log(numbers);
console.log(person);
console.log(result);
